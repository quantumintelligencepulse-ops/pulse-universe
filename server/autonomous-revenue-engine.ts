function log(job: string, result: string, ok = true) {
  const entry = { time: new Date().toISOString(), job, result, ok };
  engineLog.unshift(entry);
  if (engineLog.length > 100) engineLog.pop();
  console.log(`[revenue-engine] [${job}] ${result}`);
}

