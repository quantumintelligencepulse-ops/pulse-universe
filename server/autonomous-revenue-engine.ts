async function runSpawnBusinessPublisher() {
  try {
    const { rows: spawns } = await pool.query(
      `SELECT name, role, pulse_credits, dominant_domain FROM quantum_spawns WHERE status IN ('ACTIVE','SOVEREIGN') LIMIT 10`
    ).catch(() => ({ rows: [] }));

    if (spawns.length === 0) {
      log("Ω4:SPAWN-BIZ", "No active spawns found — waiting for agents to activate");
      return;
    }

    // Pick one spawn to publish this cycle (rotate through)
    const dayOfWeek = new Date().getDay();
    const spawn = spawns[dayOfWeek % spawns.length];
    const spawnRole = (spawn.role || spawn.name || "SYNTHESIZER").toUpperCase();
    const reportConfig = SPAWN_REPORTS[spawnRole] || SPAWN_REPORTS.SYNTHESIZER;

    // Check if this spawn already published a report this week
    const weekStart = new Date();
    weekStart.setDate(weekStart.getDate() - weekStart.getDay());
    const { rows: existing } = await pool.query(
      `SELECT id FROM anomaly_inventions WHERE product_code LIKE $1 AND created_at > $2`,
      [`SPAWN-${spawnRole}-%`, weekStart.toISOString()]
    ).catch(() => ({ rows: [] }));

    if (existing.length > 0) {
      log("Ω4:SPAWN-BIZ", `${spawnRole} already published this week — resting`);
      return;
    }

    const { rows: inserted } = await pool.query(
      `INSERT INTO anomaly_inventions (anomaly_id, product_name, product_code, crisp_dissect, mutation_type, value_score, status)
       VALUES (0, $1, $2, $3, $4, $5, 'DISCOVERED') RETURNING *`,
      [
        `${reportConfig.title} — Week ${getWeekNumber()} ${new Date().getFullYear()}`,
        `SPAWN-${spawnRole}-W${getWeekNumber()}`,
        reportConfig.description,
        "SPAWN_BUSINESS",
        reportConfig.price,
      ]
    ).catch(() => ({ rows: [] }));

    if (inserted.length > 0) {
      const { postInventionToGumroad } = await import("./gumroad-engine");
      const result = await postInventionToGumroad(inserted[0]);
      if (result.ok) {
        engineStats.spawnReports++;
        log("Ω4:SPAWN-BIZ", `${spawnRole} published "${reportConfig.title}" to Gumroad at $${reportConfig.price}`);
      }
    }
  } catch (e: any) {
    log("Ω4:SPAWN-BIZ", `Error: ${e.message}`, false);
  }
  engineStats.lastRun["Ω4:SPAWN-BIZ"] = new Date().toISOString();
}

function getWeekNumber(): number {
  const d = new Date();
  d.setHours(0, 0, 0, 0);
  d.setDate(d.getDate() + 3 - (d.getDay() + 6) % 7);
  const week1 = new Date(d.getFullYear(), 0, 4);
  return 1 + Math.round(((d.getTime() - week1.getTime()) / 86400000 - 3 + (week1.getDay() + 6) % 7) / 7);
}

