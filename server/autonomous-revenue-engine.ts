// ── Ω1: GUMROAD AUTO-POSTER ──────────────────────────────────────────────────
// Every 6 hours: find DISCOVERED inventions → auto-post to Gumroad
async function runGumroadPoster() {
  try {
    const results = await autoPostPendingInventions();
    const posted = results.filter(r => r.ok).length;
    if (posted > 0) {
      engineStats.gumroadPosted += posted;
      log("Ω1:GUMROAD", `Posted ${posted} inventions to Gumroad automatically`);
    } else {
      log("Ω1:GUMROAD", "No new inventions ready — hive producing, market waiting");
    }
  } catch (e: any) {
    log("Ω1:GUMROAD", `Error: ${e.message}`, false);
  }
  engineStats.lastRun["Ω1:GUMROAD"] = new Date().toISOString();
}

