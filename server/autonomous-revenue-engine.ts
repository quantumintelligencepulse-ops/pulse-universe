function extractKeyword(text: string): string {
  const words = text.toLowerCase().replace(/[^a-z0-9 ]/g, "").split(" ").filter(w => w.length > 4);
  const stop = new Set(["which", "there", "their", "about", "would", "could", "should", "where", "these", "those", "after", "before"]);
  const meaningful = words.filter(w => !stop.has(w));
  return meaningful.slice(0, 2).join(" ") || "quantum technology";
}

